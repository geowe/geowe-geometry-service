package org.geowe.service.model;

import java.io.Serializable;

/**
 * Represents an error in validation
 * 
 * @author rafa@geowe.org
 *
 */
public class ValidationErrorData implements Serializable {

	
	private static final long serialVersionUID = 1170267849912804032L;
	private int id;
	private String description;

	
	public ValidationErrorData() {
		super();
		
	}

	public ValidationErrorData(int id, String description) {
		super();
		this.id = id;
		this.description = description;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((description == null) ? 0 : description.hashCode());
		result = prime * result + id;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ValidationErrorData other = (ValidationErrorData) obj;
		if (description == null) {
			if (other.description != null)
				return false;
		} else if (!description.equals(other.description))
			return false;
		if (id != other.id)
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "ValidationError [id=" + id + ", description=" + description + "]";
	}

}